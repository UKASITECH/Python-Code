my_string = "Hello, World!"
print(len(my_string))  # Output: 13
 
my_string1 = "HELLO"
print(my_string1.lower())  # Output: "hello"

my_string2 = "hello"
print(my_string2.upper())  # Output: "HELLO"

my_string3 = "  Hello, World!  "
print(my_string3.strip())  # Output: "Hello, World!"

my_string4 = "I like apples"
print(my_string4.replace("apples", "bananas"))  # Output: "I like bananas"

my_string5 = "Hello,World,Python"
print(my_string5.split(","))  # Output: ['Hello', 'World', 'Python']

my_list = ["Hello", "World", "Python"]
print(" ".join(my_list))  # Output: "Hello World Python"

